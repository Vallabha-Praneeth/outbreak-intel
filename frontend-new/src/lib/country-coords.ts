/**
 * Get coordinates for a country by ISO2 code or name
 */
export function getCountryCoordinates(countryOrIso2: string): [number, number] | null {
    // Try direct ISO2 lookup
    const upper = countryOrIso2.toUpperCase()
    if (COUNTRY_COORDINATES[upper]) {
        return COUNTRY_COORDINATES[upper]
    }

    // Fallback: try to match by common country names
    const NAME_TO_ISO2: Record<string, string> = {
        'united states': 'US',
        'usa': 'US',
        'united kingdom': 'GB',
        'uk': 'GB',
        'democratic republic of the congo': 'CD',
        'drc': 'CD',
        'republic of the congo': 'CG',
        'south korea': 'KR',
        'north korea': 'KP',
        'ivory coast': 'CI',
        'cote d\'ivoire': 'CI',
    }

    const normalized = countryOrIso2.toLowerCase()
    const iso2 = NAME_TO_ISO2[normalized]
    if (iso2 && COUNTRY_COORDINATES[iso2]) {
        return COUNTRY_COORDINATES[iso2]
    }

    return null
}
